def build_complete_report(user_info):
    raw_pkg = user_info.get("package", "free")
    package = PACKAGE_ALIASES.get(raw_pkg, "free")
    user_info["package"] = package

    chapters_funcs = [
        chapter_1_blocks,
        chapter_2_blocks,
        chapter_3_blocks,
        chapter_4_blocks,
        chapter_5_blocks,
        chapter_6_blocks,
        chapter_7_blocks,
        chapter_8_blocks,
        chapter_9_blocks,
        chapter_10_blocks,
    ]

    report = {
        "package": package,
        "package_name": PACKAGES[package]["name"],
        "chapters": [],
    }

    allowed_chapters = PACKAGE_RULES[package]["chapters"]

    for idx, fn in enumerate(chapters_funcs, 1):
        if idx not in allowed_chapters:
            continue

        blocks = fn(user_info)  # الحصول على كل الـ blocks
        if blocks:
            report["chapters"].append(
                {
                    "chapter_number": idx,
                    "blocks": blocks,
                }
            )

    return report
